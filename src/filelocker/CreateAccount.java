    // create account button. Connects to database to create the new account
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        jLabel6.setIcon(new ImageIcon("ajax-loader.gif"));
        jLabel6.setText("Loading...");

        SwingWorker<Void, Void> mySwingWorker = new SwingWorker<Void, Void>() {
            @Override
            protected void done() {
                jLabel6.setIcon(null);
                jLabel6.setText("");
            }
            @Override
            protected Void doInBackground() {

                String username = jTextField1.getText(); // get user name input
                String email = jTextField2.getText(); // get email input
                String password = new String(jPasswordField1.getPassword()); // get password input
                String password2 = new String(jPasswordField2.getPassword()); // get confirm password input
                boolean userExists = DatabaseConnection.checkUsername(username);// checks whether username already exists in DB
                if (userExists) {
                    jLabel4.setText("Username already exists. Please try another.");
                    jLabel6.setIcon(null);
                    jLabel6.setText("");
                } else {
                    if (password.equals(password2) && !password.equals("")) { // checks for password mismatch
                        byte[] salt = Encrypt.salt(); // generates new salt
                        String pass = Encrypt.hash(password, salt); // hashes password and salt
                        // inserts account into database
                        DatabaseConnection.insertAccount(username, pass, email, salt);
                        MyGui.msgLabel.setForeground(Color.green);
                        MyGui.msgLabel.setText("Account Created Succesfully");

                        dispose();

                    } else {
                        jLabel4.setText("Passwords Mismatch. Please try again");
                        jLabel6.setIcon(null);
                        jLabel6.setText("");
                    }

                }
                return null;
            }

        };

        mySwingWorker.execute();
        jLabel6.setIcon(null);
        jLabel6.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

